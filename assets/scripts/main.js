document.addEventListener('DOMContentLoaded', () => {
    const langBtn = document.getElementById('lang-switch');
    let currentLang = localStorage.getItem('language') || 'en';

    const updateContent = (lang) => {
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (translations[lang][key]) {
                el.innerHTML = translations[lang][key];
            }
        });
        
        // Update WhatsApp links
        const waMsg = encodeURIComponent(translations[lang].wa_msg);
        const waUrl = `https://wa.me/5561992747373?text=${waMsg}`;
        if (document.getElementById('hero-whatsapp')) document.getElementById('hero-whatsapp').href = waUrl;
        if (document.getElementById('footer-whatsapp')) document.getElementById('footer-whatsapp').href = waUrl;

        langBtn.textContent = lang === 'en' ? 'PT' : 'EN';
        document.documentElement.lang = lang;
        localStorage.setItem('language', lang);
    };

    // Initialize
    updateContent(currentLang);

    langBtn.addEventListener('click', () => {
        currentLang = currentLang === 'en' ? 'pt' : 'en';
        updateContent(currentLang);
    });

    // Mobile Menu Toggle
    const mobileMenu = document.getElementById('mobile-menu');
    const navList = document.getElementById('nav-list');
    if (mobileMenu) {
        mobileMenu.addEventListener('click', () => {
            navList.classList.toggle('active');
        });
    }

    // Smooth scroll
    document.querySelectorAll('nav a').forEach(anchor => {
        anchor.addEventListener('click', function(e) {
            const href = this.getAttribute('href');
            if (href.startsWith('#')) {
                e.preventDefault();
                const targetId = href;
                if (navList && navList.classList.contains('active')) navList.classList.remove('active');
                const targetElement = document.querySelector(targetId);
                if (targetElement) {
                    targetElement.scrollIntoView({ behavior: 'smooth' });
                }
            }
        });
    });
});
